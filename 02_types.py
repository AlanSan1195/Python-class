#  python tiene varios tipos de datos, los mas comunes son:

# - int: numeros enteros
# - float: numeros con decimales
# - str: cadenas de texto
# - bool: valores booleanos (True o False)
# - range: rango de numeros

# enteros
print("int:")
# para saber el tipo de dato al print le agregamos le type 
print(type(42)) 
# output: 42 type: int "entero" 
# print(-42)
# print(0)
print(18436594392534546748743625872)


# decimales
print("float:")
print(type(42.0))
# output: 42.0 type: float "decimal"
# print(-42.0)  
# print(0.0)

# cadenas de texto
print("str:")
print(type('hola mundo'))
# output: hola mundo type: str "cadena de texto"


# booleanos
print("bool:")
print(type(True))
# output: True type: bool "booleano"
# print(False)

# rango de numeros
print("range:")
print(type(range(5)))
# output: range(0, 5) type: range "rango de numeros"

# complejos
print("complex:")
print(type(1 + 1j))
# output: (1+1j) type: complex "numero complejo"
# es un numero imaginario "1j"

